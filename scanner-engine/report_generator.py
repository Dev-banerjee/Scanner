    def _generate_crawled_urls(self, crawled_urls):
        self.add_page()
        self._draw_section_title("5", "Appendix: Crawled URLs")

        self.set_font('Arial', '', 9)
        self.set_text_color(*COLOR_MUTED)
        self.cell(0, 6, self.sanitize(f"Total URLs discovered: {len(crawled_urls)}"), 0, 1)
        self.ln(4)

        for idx, url in enumerate(crawled_urls, 1):
            self._ensure_space(7)

            row_y = self.get_y()
            if idx % 2 == 0:
                self.set_fill_color(*COLOR_CARD)
                self.rect(10, row_y, 190, 6, 'F')

            self.set_font('Arial', '', 7)
            self.set_text_color(*COLOR_MUTED)
            self.cell(10, 6, f"{idx:>3}.", 0, 0)
            self.set_text_color(*COLOR_ACCENT)
            url_text = self.sanitize(str(url))
            if len(url_text) > 120:
                url_text = url_text[:117] + '...'
            self.cell(0, 6, url_text, 0, 1)
